package com.javaLoops;

public class Example2 {

	public static void main(String[] args) {
		/**
		 * 2x1 = 2
		 * 2x2 = 4
		 * 2x3 = 6
		 * .
		 * .
		 * .
		 * 2x10 = 20
		 */
		
		int number = 7;
		
		for(int i = 1; i <= 10; i++)
		{
			System.out.println(number+"x"+i+" = "+i*number);
		}
		

	}

}
